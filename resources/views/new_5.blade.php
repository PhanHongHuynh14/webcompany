    </div>
    <div class="content_1">
        <p>
        Tiếp nối đà tăng trưởng trong năm 2021, ngành thép Việt Nam bước vào năm 2022 với những tín hiệu lạc quan khi
        kinh tế dần hồi phục sau đại dịch Covid-19. Các doanh nghiệp thép có thêm cơ hội để thúc đẩy sản xuất, xuất khẩu
        ra các thị trường châu Âu và Mỹ. Tuy nhiên, lĩnh vực này cũng đang đối diện với ba rủi ro lớn.
    </p> <p>
        Năm 2021 được xem là một năm rực rỡ của ngành thép khi các doanh nghiệp thép trong nước đều đạt lợi nhuận cao.
        Tổng sản lượng của ngành thép trong năm 2021 đạt 30,8 triệu tấn, tăng 32,5% so với năm 2020. Trong đó, sản lượng
        xuất khẩu thép toàn ngành đạt 6 triệu tấn, tăng 52,5% so với năm trước, giá trị xuất khẩu đạt 12 tỉ USD, cao
        nhất từ trước tới nay.
    </p> <p>
        Nhờ sự tăng giá của thép cuộn cán nóng (HRC) trong năm 2021, các doanh nghiệp ngành thép đều gia tăng biên lợi
        nhuận gộp từ 3-6% nhờ chênh lệch giá nguyên liệu đầu vào. Cổ phiếu ngành thép cũng đã diễn biến tích cực. Cụ
        thể, các cổ phiếu như HPG, NKG hay HSG đã tăng lần lượt là 100%, 366% và 163% trước khi điều chỉnh do áp lực
        giảm giá của HRC trong 2 tháng cuối năm 2021.
    </p> <b>
        Cơ hội tăng trưởng
</b><p>
        Trong báo cáo triển vọng ngành thép năm 2022, Công ty chứng khoán Mirae Asset – MASVN đã đưa ra góc nhìn trung
        lập đối với ngành thép trong năm 2022. Theo đó, MASVN vẫn tiếp tục đánh giá tích cực cho cả ngành thép trong năm
        nay.
    </p> <p>
        Công ty Mirae Asset cho rằng ngành bất động sản và xây dựng sẽ phục hồi, qua đó sẽ thúc đẩy sản lượng toàn ngành
        thép. Bên cạnh đó, thị trường xuất khẩu sẽ tiếp tục rộng mở trong năm 2022. Việc thiếu hụt nguồn cung trầm trọng
        ở châu Âu sẽ giúp các công ty thép Việt Nam sẽ tiếp tục hưởng lợi từ xuất khẩu.
    </p> <p>
        Hiệp hội Thép Việt Nam (VSA) cho rằng triển vọng thị trường thép Việt Nam năm 2022 sẽ tốt hơn khi Chính phủ ban
        hành chỉ đạo ổn định và phát triển các hoạt động sản xuất kinh doanh thích ứng linh hoạt với đại dịch Covid-19.
    </p> <p>
        Những trọng tâm được Chính phủ xác định trong năm 2022 gồm khôi phục, thúc đẩy sản xuất kinh doanh; đẩy mạnh
        xuất khẩu và đẩy nhanh tiến độ giải ngân vốn đầu tư công, huy động mọi nguồn lực phát triển hạ tầng sẽ là trợ
        lực cho ngành thép phát triển mạnh hơn trong năm 2022.
    </p> <p>
        MASVN nhận định trong năm 2022, sản lượng thép trong nước sẽ khó có tăng trưởng đột biến như năm 2021 khi hầu
        hết các doanh nghiệp thép nội địa đã chạy hết công suất và chưa có những đại dự án mới đưa vào hoạt động trong
        năm nay. MASVN dự báo sản lượng thép toàn ngành năm 2022 sẽ tăng 8%, ước tính đạt 33,3 triệu tấn. Riêng sản
        lượng thép xuất khẩu đạt mức 8,7 triệu tấn, tăng 15% so với năm 2021.
    </p> <p>
        Cùng với đầu tư công, sự nóng lên của thị trường bất động sản nhà ở sẽ giúp sản lượng tiêu thụ thép xây dựng
        tăng 10-15% so với cùng kỳ vào năm 2022.
    </p> <p>
        Đối với mặt hàng tôn mạ, Công ty Mirae Asset đánh giá các công ty Tôn Nam Kim, Tôn Hoa Sen và Tôn Đông Á sẽ được
        hưởng lợi ở mảng xuất khẩu sản phẩm này. Nguyên nhân là do mảng tôn mạ hiện tại đang dư khoảng 30% tổng công
        suất trong khi nhu cầu nhập khẩu mặt hàng CRC và tôn mạ màu rất lớn khi nguồn cung từ Trung Quốc và Nga giảm
        mạnh.
    </p> <p>
        Cũng theo MASVN, trong năm 2022, thị trường xuất khẩu được mở rộng khi nguồn cung thép toàn cầu giảm do tác động
        của xung đột giữa Nga – Ukraine. Nga hiện đang xếp thứ 2 về xuất khẩu thép vào EU, với tỷ trọng 14.1% thép dẹt
        và 19% thép dài. Ukraine chiếm 8% thép dẹt và 7.4% thép dài, còn Belarus chiếm 14.4% thép dài.
    </p> <p>
        Việc thiếu hụt nguồn cung trầm trọng sẽ giúp các doanh nghiệp ngành thép trong nước sẽ tiếp tục hưởng lợi từ
        xuất khẩu, đặc biệt từ thị trường châu Âu và Mỹ.
    </p> <b>
        Và những thách thức
    </b> <p>
        Việc thúc đẩy giải ngân vốn đầu tư công được cho là tác động tích cực đến thị trường thép và vật liệu xây dựng.
        Tuy nhiên, ngành thép Việt Nam cần thận trọng trong sản xuất, xuất khẩu do dự báo giá thép có sự điều chỉnh giảm
        khi cung-cầu trên thế giới trở nên cân bằng hơn. Bên cạnh đó, ngành thép cũng phải đối diện ngày càng nhiều với
        rủi ro bị kiện phòng vệ thương mại do xu thế bảo hộ đang gia tăng trên thế giới.
    </p> <p>
        MASVN nhận định ngành thép trong nước đang đối diện với ba rủi ro lớn. Đó là rủi ro về biến động giá nguyên vật
        liệu; rủi ro về thuế chống bán phá giá thị trường xuất khẩu; rủi ro về hạn chế xuất khẩu.
    </p> <p>
        Theo đó, ngành thép và tôn mạ có rủi ro lớn về biến động giá nguyên vật liệu khi chi phí nguyên liệu chiếm
        65-75% giá thành sản xuất. Đặc biệt trong ngành tôn mạ, giá HRC chiếm hơn 80% chi phí nguyên liệu đầu vào, khiến
        lợi nhuận của cả ngành biến động rất lớn theo HRC.
    </p> <p>
        Ngoài ra, trong bối cảnh giá than cốc đã tăng rất mạnh cùng với giá quặng sắt liên tục có xu hướng tăng giá có
        thể khiến thị trường xây dựng suy giảm tốc độ tăng trưởng, qua đó ảnh hưởng trực tiếp tới sản lượng.
    </p> <p>
        Một yếu tố khác cũng tác động đến ngành thép trong năm 2022 là rủi ro về thuế chống bán phá giá thị trường xuất
        khẩu.
    </p> <p>
        Kim ngạch xuất khẩu của ngành thép chiếm 19,56% tổng sản lượng bán hàng. Vì vậy, vẫn tồn tại rủi ro rất lớn khi
        chính sách thuế quan thay đổi trong bối cảnh chiến tranh thương mại vẫn diễn ra giữa Trung Quốc và các nước trên
        thế giới. Chính vì vậy, các doanh nghiệp cũng cần chuẩn bị tốt kiến thức về thương mại quốc tế để chủ động ứng
        phó với nguy cơ kiện cáo, phòng vệ thương mại tại các thị trường xuất khẩu.
    </p> <p>
        Hiện nay, giá thép xây dựng đã tăng lên mức 18,3 triệu đồng/tấn. MASVN đánh giá rủi ro có thể xảy ra nếu giá vật
        liệu xây dựng ở mức cao, một số dòng thép sẽ bị hạn chế xuất khẩu. Trong đó, phôi thép xây dựng là dòng sản phẩm
        sẽ đối diện với nguy cơ này đầu tiên.
    </p> <p>
        Theo số liệu mới nhất của VSA, sản xuất thép thành phẩm của Việt Nam trong 2 tháng đầu năm 2022 đạt 5,117 triệu
        tấn, tăng 1,6% so với cùng kỳ năm 2021. Bán hàng thép thành phẩm đạt 5,014 triệu tấn, tăng 18,5% so với cùng kỳ
        năm 2021. Trong đó, xuất khẩu đạt 126.017 tấn, tăng 11,6% so với cùng kỳ năm ngoái.
    </p> 
